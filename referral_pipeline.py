user_referral_logs_df = user_referral_logs.rename(columns={'id': 'user_referral_log_id'})
user_logs_df = user_logs.rename(columns={'id': 'user_log_id'})
referral_rewards_df = referral_rewards.rename(columns={'id': 'reward_pk_id'}) # Renaming to avoid conflict with referral_reward_id in user_referrals
