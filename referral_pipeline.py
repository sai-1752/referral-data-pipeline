df = df.merge(
    lead_logs_df,
    left_on="referee_id",
    right_on="lead_id",
    how="left"
)

def get_source_category(row):

    if row["referral_source"] == "User Sign Up":
        return "Online"

    elif row["referral_source"] == "Draft Transaction":
        return "Offline"

    elif row["referral_source"] == "Lead":
        return row["source_category"]

    else:
        return "Unknown"


df["referral_source_category"] = df.apply(get_source_category, axis=1)

def validate_referral(row):

    if (
        row["reward_value"] > 0
        and row["description"] == "Berhasil"
        and row["transaction_status"] == "PAID"
        and row["transaction_type"] == "NEW"
        and row["transaction_at"] > row["referral_at"]
        and row["membership_expired_date"] > row["referral_at"]
        and row["is_deleted"] == False
        and row["is_reward_granted"] == True
    ):
        return True

    return False

# Ensure the column is string type before applying .str operations
df["reward_value"] = df["reward_value"].astype(str).str.replace(" days", "", regex=False)
# Convert to numeric, coerce errors to NaN, fill NaN with 0, then convert to int
df["reward_value"] = pd.to_numeric(df["reward_value"], errors='coerce').fillna(0).astype(int)
df["is_business_logic_valid"] = df.apply(validate_referral, axis=1)

